#Arithmetic Operators - Simple Calculations
num1 = 45
num2 = 20

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2) #Modulus - returns the remainder

#Comparison Operators
x = 78
y = 60
print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x != y)

#Assignment Operators - Used to assign values to a variable
number = 40
print(number)

paper = 35
paper += 2
print(paper)
paper -= 2
print(paper)

result = 90 - (6 * 3) / 2 + 5 #Operator Precedence
print(result)

#Logical Operators - and, or, not
print(90 < 40 and 67 > 34)
print(90 < 40 or 67 > 34)
print(not(90 < 40 and 67 > 34))







