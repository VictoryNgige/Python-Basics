#A Python Program that Checks whether a Number is Even or Odd
n = int(input("Enter the number: "))

if n % 2 == 0:
    print(n, "is an even number.")
else:
    print(n, "is an odd number.")
print()

#A Python Program that Checks whether a Letter is a Vowel or a Consonant
l = input("Enter the Letter:").lower()

if l == "a" "e" "i" "o" "u":
    print(l, "is a vowel")
else:
    print(l, "is a consonant")
print()

